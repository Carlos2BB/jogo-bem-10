const PIXEL = 8;

function drawSprite(ctx, x, y, sprite, colors) {
    for (let r = 0; r < 8; r++) {
        for (let c = 0; c < 8; c++) {
            const px = sprite[r][c];
            if (px) {
                ctx.fillStyle = colors[px];
                ctx.fillRect(x + c * PIXEL, y + r * PIXEL, PIXEL, PIXEL);
            }
        }
    }
}

